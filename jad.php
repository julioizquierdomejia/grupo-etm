	<p>
	El día jueves 4 de setiembre, Editorial Tercer Milenio compartió junto con directores de prestigiosos colegios de nuestra ciudad la Jornada de Actualización Directiva, JAD 2014.  Esta reunión se desarrolló en torno al tema “El liderazgo en la búsqueda de la excelencia educativa”.
	</p>
	<p>
		Contamos con la importante experiencia del Sr. Jaime Millás Mur, Master en Dirección de Centros Educativos, quien se encargó de puntualizar la tarea de un director en el colegio. Dio a conocer la importancia de la formación personal y académica continua de un directivo; la habilidad de escucha que este debe desarrollar para nutrir su tarea en favor del colegio con los aportes de su equipo educador; también mencionó la necesidad de formar convenios con otras instituciones educativas para compartir las experiencias positivas y adaptarlas a su propia realidad en beneficio de los estudiantes y las familias. Un tiempo para la reflexión sobre su quehacer es vital para renovarse, resaltó nuestro primer expositor invitado.
	</p>
	<p>
		El Dr. Pablo Ferreiro de Babot, experto en el Área de Gobierno de Personas del PAD Escuela de Dirección de la Universidad de Piura, fue el encargado de recordarnos el concepto de líder como aquella persona capaz de orientar a su grupo de seguidores hacia la resolución efectiva de problemas a través de soluciones que no atenten contra la dignidad de la persona. El Dr. Ferreiro, además, mencionó que el líder es modelo, ya que contagia sus valores mediante su actuar coherente y ético. Destacó que el director líder ofrece a sus seguidores el espacio para descubrir sus propias competencias y las oportunidades para aportar lo mejor de sí mismos al centro educativo. Concluyó su exposición resaltando que el líder es aquel que entiende su tarea como un servicio a los demás y la hace con alegría.
	</p>
	<p>
		Nuestro tercer expositor invitado, Sr. Walter Espinoza Jock, director de Orientación y Familia del Colegio Alpamayo, disertó sobre las relaciones colegio-familia. Él señaló que la comunicación entre ambos es un factor de suma importancia para el desarrollo armónico de los alumnos. Una comunicación asertiva que oriente a los padres en la educación de sus hijos, es necesaria para lograr los objetivos de cada etapa escolar. Generar la participación de los padres en la escuela fortalece los vínculos y la confianza mutua, señaló finalmente.
	</p>
	<p>
		Durante el intermedio todos los participantes recibieron el Catálogo 2015, destacaron la flamante colección de inglés, Way to go y la renovada colección de Matemática para Secundaria, entre todas las que ETM ofrece a los colegios.
	</p>
